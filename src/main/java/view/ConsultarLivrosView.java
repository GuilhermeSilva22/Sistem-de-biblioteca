    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) tbl_livros.getModel();
            int nLinha = tbl_livros.getSelectedRow();
            LivroRepository livro = new LivroRepository();
            LivroModel l = new LivroModel();
            String titulo = (String) model.getValueAt(nLinha, 0);
            l.setTitulo(titulo);
            livro.Deletar(l);
            model.removeRow(nLinha);
        } catch (SQLException ex) {
            Logger.getLogger(ConsultarLivrosView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ConsultarLivrosView.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(ConsultarLivrosView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_excluirActionPerformed

