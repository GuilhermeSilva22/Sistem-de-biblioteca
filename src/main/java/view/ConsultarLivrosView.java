    private void CarregarTabela() throws HeadlessException {
        try {
            LivroRepository livros = new LivroRepository();
            LivroModel l = new LivroModel();
            DefaultTableModel model = (DefaultTableModel) tbl_livros.getModel();
            ArrayList<LivroModel> listaLivros = (ArrayList<LivroModel>) livros.BuscarTodos();
            for (LivroModel livro : listaLivros) {
                String titulo = livro.getTitulo();
                int quantidade = livro.getQuantidade();
                String estante = livro.getEstante();
                String prateleira = livro.getPrateleira();
                model.addRow(new Object[]{titulo, quantidade, estante, prateleira});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar a lista de Livros");
        }
    }

