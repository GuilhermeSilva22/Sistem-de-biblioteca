    private void btn_cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cadastrarActionPerformed
        AlunoModel aluno = new AlunoModel();
        LivroModel livro = new LivroModel();
        EmprestimoModel emprestimo = new EmprestimoModel();
        aluno.setNome(txt_nome.getText());
        aluno.setCpf(txt_cpf.getText());
        aluno.setTelefone(txt_telefone.getText());
        aluno.setEmail(txt_email.getText());
        aluno.setTurma(txt_turma.getText());
        aluno.setTurno(txt_turno.getText());
        livro.setTitulo(txt_titulo.getText());
        String c = txt_quantidade.getText();
        int i = Integer.parseInt(c);
        livro.setQuantidade(i);
        String data_emprestimo = txt_emprestimo.getText();
        String data_vencimento = txt_vencimento.getText();
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate data_empres = LocalDate.parse(data_emprestimo, formato);        
        LocalDate data_vence = LocalDate.parse(data_vencimento, formato);        
        emprestimo.setDataEmprestimo(data_empres);
        emprestimo.setDataVencimento(data_vence);
    }//GEN-LAST:event_btn_cadastrarActionPerformed

